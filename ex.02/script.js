let num1 = prompt("Digite o Nº 1");
let num2 = prompt("Digite o Nº 2");

// Templantes string
console.log(`O valor de Nº 1 é  ${num1}`);
console.log(`O valor de Nº 2 é  ${num2}`);

// Concatenação
console.log("O valor de Nº 1 é " + num1 );
console.log("O valor de Nº 2 é " + num2 );

// Soma
console.log(`A soma de ${num1} + ${num2} é igual a ${num1 + num2} `);
// Subtração
console.log(`A sub de ${num1} - ${num2} é igual a ${num1 - num2} `);
// Multiplicação
console.log(`A Mult de ${num1} x ${num2} é igual a ${num1 * num2} `);
// Divisão
console.log(`A div de ${num1} / ${num2} é igual a ${num1 / num2} `);

